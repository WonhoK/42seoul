char	*ft_strlowcase(char *str)
{
	int	diff;
	int	i;

	diff = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] -= diff;
		i++;
	}
	return (str);
}
